package com.javarush.task.task03.task0314;

/* 
Таблица умножения
*/

public class Solution {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++){
            for (int x = 1; x <= 10; x++){
                System.out.print((i * x) + " ");//напишите тут ваш код
            }
            System.out.println();
        }
    }
}
